def generate_full_essay():
    # 500자 이상을 위해 두 조각씩 붙이거나 변형을 줍니다.
    def enrich(text):
        # 약간의 변형을 주어 고유성을 높임
        fillers = [
            " 저는 이러한 경험이 바탕이 되어 실무에서도 빛을 발할 것이라 확신합니다.",
            " 이를 통해 배우고 느낀 점은 제 인생의 큰 자산입니다.",
            " 항상 앞선 생각으로 더 나은 결과를 만들어내겠습니다.",
            " 믿고 지켜봐 주시면 결과로 증명해 보이겠습니다.",
            " 귀사의 목표 달성을 위해 제 모든 역량을 집중하겠습니다."
        ]
        return text + random.choice(fillers) + " " + text[:random.randint(200, 300)]

    return (
        enrich(random.choice(growth_templates)),
        enrich(random.choice(strength_templates)),
        enrich(random.choice(job_templates)),
        enrich(random.choice(motivation_templates))
    )

def main():
    sql_file = "/home/foenix/workspace/spring-rag/src/main/resources/populate_data.sql"
    with open(sql_file, "w", encoding="utf-8") as f:
        f.write("DELETE FROM test.rec_appli_mas;\n") # 기존 데이터 초기화
        for i in range(1, 101):
            accept_no = f"2026_{i:03d}"
            name = generate_random_name()
            e1, e2, e3, e4 = generate_full_essay()
            
            # SQL 인젝션 방지는 아니지만 샘플 데이터 생성이므로 간단히 작은따옴표 이스케이프
            e1 = e1.replace("'", "''")
            e2 = e2.replace("'", "''")
            e3 = e3.replace("'", "''")
            e4 = e4.replace("'", "''")
            
            sql = f"INSERT INTO test.rec_appli_mas (acceptno, name, hsgessay1, hsgessay2, hsgessay3, hsgessay4) VALUES ('{accept_no}', '{name}', '{e1}', '{e2}', '{e3}', '{e4}');\n"
            f.write(sql)
    
    print(f"Generated 100 entries in {sql_file}")

if __name__ == "__main__":
    main()
